const drop1 = document.querySelectorAll(".drop select")

let button = document.querySelector("form button")
let fromCurr = document.querySelector(".from select")
let toCurr = document.querySelector(".to select")
let msg = document.querySelector(".msg")



window.addEventListener("load",()=>{
    updateExchangeRate()
})



for (let num of drop1) {
  for (currcode in countryList) {
    let newOption = document.createElement("option")
    newOption.innerText = currcode;
    newOption.value = currcode;
    num.append(newOption)





    if (num.name === "from" && currcode === "USD") {
      newOption.selected = "selected"
    } else if (num.name === "to" && currcode === "INR") {
      newOption.selected = "selected"
    }
  }

  num.addEventListener("change", (evt) => {
    updateFlag(evt.target)
  })

 
}

const updateFlag = (element) => {
  let currcode1 = element.value
  let countryCode1 = countryList[currcode1]
  let imgLink = `https://flagsapi.com/${countryCode1}/flat/64.png`
  let img = element.parentElement.querySelector("img")
  img.src = imgLink
}


button.addEventListener("click", async (evt) => {
  evt.preventDefault()
 updateExchangeRate()


})

const updateExchangeRate= async()=>{
  let money = document.querySelector(".Money input")
  let monval = money.value
  if (monval === "" || monval < 1) {
    monval = 1
    monval.value = "1"
  }

  const url1 = `${url}/${fromCurr.value.toLowerCase()}/${toCurr.value.toLowerCase()}.json`

  let response = await fetch(url1)

  let data = await response.json()

  let rate = data[toCurr.value.toLowerCase()]
  console.log(rate)
  let finalAmout = monval * rate;
  msg.innerText = `${monval} ${fromCurr.value.toLowerCase()} = ${finalAmout} ${toCurr.value} `

}




